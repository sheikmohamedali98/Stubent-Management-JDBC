package com.management.ms.common;

public class Address {

		private int floatNo;
		private String apparmentName;
		private String streetName;
		private String city;
		private String state;
		private int pincode;
		public int getFloatno() {
			return floatNo;
		}
		public void setFloatno(int floatNo) {
			this.floatNo = floatNo;
		}
		public String getApparmentName() {
			return apparmentName;
		}
		public void setApparmentName(String apparmentName) {
			this.apparmentName = apparmentName;
		}
		public String getStreetName() {
			return streetName;
		}
		public void setStreetName(String streetName) {
			this.streetName = streetName;
		}
		public String getCity() {
			return city;
		}
		public void setCity(String city) {
			this.city = city;
		}
		public String getState() {
			return state;
		}
		public void setState(String state) {
			this.state = state;
		}
		public int getPincode() {
			return pincode;
		}
		public void setPincode(int pincode) {
			this.pincode = pincode;
		}

	}

